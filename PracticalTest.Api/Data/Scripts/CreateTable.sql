CREATE TABLE Transactions (
    TransactionID VARCHAR2(50) PRIMARY KEY,
    AccountID VARCHAR2(50) NOT NULL,
    TransactionAmount NUMBER(18,2) NOT NULL,
    TransactionCurrencyCode VARCHAR2(3) NOT NULL,
    LocalHour NUMBER(2) NOT NULL,
    TransactionScenario VARCHAR2(1) NOT NULL,
    TransactionType VARCHAR2(1) NOT NULL,
    TransactionIPaddress VARCHAR2(15) NOT NULL,
    IpState VARCHAR2(50),
    IpPostalCode VARCHAR2(20),
    IpCountry VARCHAR2(2) NOT NULL,
    IsProxyIP NUMBER(1) NOT NULL,
    BrowserLanguage VARCHAR2(10) NOT NULL,
    PaymentInstrumentType VARCHAR2(20) NOT NULL,
    CardType VARCHAR2(20),
    PaymentBillingPostalCode VARCHAR2(20),
    PaymentBillingState VARCHAR2(50),
    PaymentBillingCountryCode VARCHAR2(2),
    ShippingPostalCode VARCHAR2(20),
    ShippingState VARCHAR2(50),
    ShippingCountry VARCHAR2(2),
    CvvVerifyResult VARCHAR2(1),
    DigitalItemCount NUMBER(3) NOT NULL,
    PhysicalItemCount NUMBER(3) NOT NULL,
    TransactionDateTime TIMESTAMP NOT NULL
); 
